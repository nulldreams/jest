it('sum two numbers', () => {
  const a = 5
  const b = 10

  expect(a + b).toBe(15)
})

it('sum three numbers', () => {
  const a = 5
  const b = 10
  const c = 15

  expect(a + b + c).toBe(30)
})

it('sum four numbers', () => {
  const a = 5
  const b = 10
  const c = 15
  const d = 20

  expect(a + b + c + d).toBe(50)
})
